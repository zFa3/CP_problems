#include <bits/stdc++.h>
#define ll long long
using namespace std;

void solution(){
    ll a;
    cin>>a;
    for (size_t i = 1; i <= a; i++)
    {
        cout<<i<<' ';
    }
    cout<<'\n';    
}

int main(){
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    ll t;
    cin>>t;

    while(t--){
        solution();
    }
}